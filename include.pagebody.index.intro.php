        <h2>Swish-Katalogen</h2>
        <p>Swish-Katalogen är en katalog eller enkel sök-motor, genom att alla samlade Swish-123 nummer är kategorieserade med minst en kategori, kan dessa sökas fram genom att klicka runt i tag-molnet.<br>Katalogen har skapats på grund av bristen av sökbarhet och ren nyfikenhet, "vem har Swish-nummer 123 xxx xx xx?!" eller bara kolla swish nummer. Vill du hjälpa till att lägga till fler nummer, eller har ändringar går det alldeles utmärkt att göra det genom att göra en Pull-Request på Github. Mer info nedan.</p>
